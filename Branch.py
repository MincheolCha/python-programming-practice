#if, elif, else, while

while True:
    print("please input number between 0 and 4")
    number = int(input())
    if number == 0:
        print("차민철")
    elif number == 1:
        print("차승철")
    elif number == 2:
        print("차호철")
    elif number == 3:
        print("끝낸당")
        break
    else:
        print("없음")
